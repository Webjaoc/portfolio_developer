const htmlElement = document.documentElement;
const themeToggle = document.getElementById("theme-toggle");
const languageButtons = document.querySelectorAll("[data-language]");
const translatableNodes = document.querySelectorAll("[data-section][data-value]");
const descriptionMeta = document.querySelector('meta[name="description"]');
const yearElement = document.getElementById("year");

const storageKeys = {
    language: "portfolio-language",
    theme: "portfolio-theme"
};

let currentLanguage = localStorage.getItem(storageKeys.language) || "es";
let currentTheme = localStorage.getItem(storageKeys.theme) || "light";

const applyTranslations = (language) => {
    const dictionary = translations[language] || translations.es;

    translatableNodes.forEach((node) => {
        const section = node.dataset.section;
        const value = node.dataset.value;
        const translatedValue = dictionary[section]?.[value];

        if (translatedValue) {
            node.textContent = translatedValue;
        }
    });

    document.title = dictionary.meta.title;
    descriptionMeta.setAttribute("content", dictionary.meta.description);
    htmlElement.lang = language;
};

const setActiveLanguageButton = (language) => {
    languageButtons.forEach((button) => {
        const isActive = button.dataset.language === language;

        button.classList.toggle("is-active", isActive);
        button.setAttribute("aria-pressed", String(isActive));
    });
};

const setTheme = (theme) => {
    currentTheme = theme === "dark" ? "dark" : "light";
    document.body.dataset.theme = currentTheme;
    localStorage.setItem(storageKeys.theme, currentTheme);
};

const revealElements = document.querySelectorAll(".reveal");

const setRevealBehavior = () => {
    if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
        revealElements.forEach((element) => element.classList.add("is-visible"));
        return;
    }

    const observer = new IntersectionObserver(
        (entries, currentObserver) => {
            entries.forEach((entry) => {
                if (!entry.isIntersecting) {
                    return;
                }

                entry.target.classList.add("is-visible");
                currentObserver.unobserve(entry.target);
            });
        },
        {
            threshold: 0.16
        }
    );

    revealElements.forEach((element) => observer.observe(element));
};

languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
        currentLanguage = button.dataset.language;
        localStorage.setItem(storageKeys.language, currentLanguage);
        setActiveLanguageButton(currentLanguage);
        applyTranslations(currentLanguage);
    });
});

themeToggle.addEventListener("click", () => {
    setTheme(currentTheme === "light" ? "dark" : "light");
});

setTheme(currentTheme);
setActiveLanguageButton(currentLanguage);
applyTranslations(currentLanguage);
setRevealBehavior();

yearElement.textContent = new Date().getFullYear();
